# 실수형 데이터 중 지수 표현 방식 - e or E
# 0의 개수가 많은 경우, 지수표현법을 쓰면 실수할 확률이 적어진다(직접 다 적는 경우보다)
# 무한(INF)을 의미 - 10억
c = 1e9
#  752.5
c = 75.25e1
# 3.954
c = 3954e-3

