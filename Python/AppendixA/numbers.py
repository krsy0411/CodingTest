# 컴퓨터는 실수를 정확하게 표현해내지 못한다
d = 0.3 + 0.6
print(d)

if d == 0.9:
    print(True)
else:
    print(False)

# 따라서 소수점 값을 비교하는 작업이 필요하다면 - round() 필요
# round() - 첫번째 인자: 실수형 데이터, 두번째 인자: (반올림하고자 하는 위치 -1)
e = 0.3 + 0.6
# e변수값을 5번째 자리에서 반올림
print(round(e,4))

if round(e,4) == 0.9:
    print(True)
else:
    print(False)

